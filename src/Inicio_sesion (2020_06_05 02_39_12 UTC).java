    public void Iniciarsesion(){
        
        String contraseña=jPasswordField1.getText();
        String usuario=jTextField1.getText();
        String SQL="select * from usuarios where usuario='"+usuario+"' and contraseña='"+contraseña+"'";
        
        try{
            boolean res=false;
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
                if(rs.next()){
                    res=true;
                }
                    if (res==false){
                        JOptionPane.showMessageDialog(null,"No se encontro el dato");
                    }
                    if(rs.getString("contraseña").equals(jPasswordField1.getText())){
                        JOptionPane.showMessageDialog(null,"Acceso permitido");
                        Pagina_inicio dc=new Pagina_inicio();  
                        dc.setVisible(true);
                        this.dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Contraseña o Usuario incorrectos");
                        jTextField1.setText(" ");
                        jPasswordField1.setText("");
                    }
                
                
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error");
        }
}
    public void Agregarusuario(){
   
        String SQL="insert into usuarios (usuario,contraseña) values (?,?);";
            try{
                PreparedStatement pat=con.prepareStatement(SQL);
                pat.setString(1,jTextField1.getText());
                pat.setString(2,jPasswordField1.getText());
                
                pat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Registro Exitoso");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"hola");
            }
    }
